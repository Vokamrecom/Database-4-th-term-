SELECT        Заказы.[Наименование товара], Заказы.[Дата поставки]
FROM            Заказчики INNER JOIN
                         Заказы ON Заказчики.[Наименование фирмы] = Заказы.Заказчик INNER JOIN
                         Товары ON Заказы.[Наименование товара] = Товары.Наименование
WHERE        (Заказы.[Дата поставки] > CONVERT(DATETIME, '2018-01-01 00:00:00', 102)) AND
                         (Заказы.[Дата поставки] < CONVERT(DATETIME, '2018-05-31 00:00:00', 102))